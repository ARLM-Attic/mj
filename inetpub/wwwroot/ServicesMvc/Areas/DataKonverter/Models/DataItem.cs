using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace ServicesMvc.Areas.DataKonverter.Models
{
    public class DataItem
    {
        public string Input { get; set; }
        public DataType DestDataType { get; set; }

        public string OutputString { get; set; }
        public double OutputDouble { get; set; }
        public short OutputInt { get; set; }            // Int16
        public long OutputLong { get; set; }
        public DateTime OutputDateTime { get; set; }

        public enum DataType
        {
            String = 1,
            Double = 2,
            Int = 3,
            Long = 4,
            DateTime = 5
        }
    }


}